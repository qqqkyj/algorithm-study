package BOJ.basic1;

import java.io.*;

// 풀지 못한 문제
// 참고 링크: https://eunchaan.tistory.com/51

public class Boj2133 {
  public static void main(String[] args) throws Exception {
    BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
    int n = Integer.parseInt(br.readLine());
    System.out.print(go(n));
  }

  static int go(int n) {
    int[] dp = new int[n + 1]; // dp[i] = 3×i 크기를 채우는 경우의 수
    
    // -----------------------------
    // 🔥 기본 성질
    // -----------------------------
    // 3×N에서 N이 홀수면 절대 채울 수 없음
    // 이유: 타일은 항상 2칸 차지 → 전체 칸 수가 짝수여야 함
    // 3×홀수 = 홀수 → 불가능
    // 따라서 dp[홀수] = 0
    if (n % 2 != 0)
      return 0;

    dp[0] = 1; // 아무것도 채우지 않는 경우 → 1 (DP 시작점)
    dp[1] = 0; // 홀수 → 불가능
    dp[2] = 3; // 3×2는 직접 그려보면 3가지 존재

    // -----------------------------
    // 🔥 점화식 이해 (핵심)
    // -----------------------------
    // dp[4]를 생각해보면:
    //
    // 👉 기본 구조:
    // dp[2] 상태에서 오른쪽에 2칸을 붙이는 방법
    // → dp[2] * 3 = 3 * 3 = 9
    //
    // 👉 그런데 여기서 끝이 아님!
    // "ㄱ 모양" 같은 특수한 구조 2개가 추가됨
    // → 총 9 + 2 = 11
    //
    // -----------------------------
    // dp[6]도 같은 방식:
    //
    // 👉 기본:
    // dp[4] * 3 = 11 * 3 = 33
    //
    // 👉 이전 특수 구조 활용:
    // dp[2]에서 만들어진 특수 구조를 뒤에 붙일 수 있음
    // → dp[2] * 2 = 3 * 2 = 6
    //
    // 👉 완전히 새로운 특수 구조:
    // → +2
    //
    // 👉 총합:
    // 33 + 6 + 2 = 41
    //
    // -----------------------------
    // 💡 핵심 패턴 정리
    //
    // dp[i] = dp[i-2] * 3          ← 기본 확장
    //       + dp[i-4] * 2
    //       + dp[i-6] * 2
    //       + dp[i-8] * 2
    //       ...
    //       + dp[0] * 2
    //
    // 👉 "특수 구조"는 항상 2개씩 추가됨
    // 👉 이전 dp들을 계속 참조하면서 누적

    for (int i = 4; i <= n; i+=2) {
      // 🔹 기본 확장
      // dp[i-2] 상태에서 2칸을 붙이는 3가지 방법
      dp[i] = dp[i - 2] * 3;

      // 🔹 특수 케이스 누적
      // (i-4), (i-6), ... (0) 까지 확인하면서
      // 각각에서 만들 수 있는 "특수 모양" 2개씩 추가
      for (int j = i - 4; j >= 0; j-=2) {
        dp[i] += dp[j] * 2;
      }
    }
    
    return dp[n];
  }
}